/**
 * Color scheme used by the OS and by color themes.
 */
export var ColorScheme;
(function (ColorScheme) {
    ColorScheme["DARK"] = "dark";
    ColorScheme["LIGHT"] = "light";
    ColorScheme["HIGH_CONTRAST"] = "hc";
})(ColorScheme || (ColorScheme = {}));
export var ColorThemeMode;
(function (ColorThemeMode) {
    ColorThemeMode["dark"] = "dark";
    ColorThemeMode["light"] = "light";
})(ColorThemeMode || (ColorThemeMode = {}));
export var ColorThemeEvent;
(function (ColorThemeEvent) {
    ColorThemeEvent["onChange"] = "colorTheme.onChange";
})(ColorThemeEvent || (ColorThemeEvent = {}));
